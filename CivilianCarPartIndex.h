#pragma once

#ifndef CIVILIANCARPARTINDEX_H_
#define CIVILIANCARPARTINDEX_H_

/**
* @brief Enumeration of civilian car part indexes.
*/
enum class CivilianCarPartIndex
{
    None,           /// No index needed.
    HeadLight_L,    /// Headlight Left.
    HeadLight_R,    /// Headlight Right.
    RearLight_L,    /// Rearlight Left.
    RearLight_R,    /// Rearlight Right.
    COUNT           /// Number of elements.
};

#endif // CIVILIANCARPARTINDEX_H_